def _write_tsv(path: Path, rows: List[List[str]], header: List[str]) -> None:
    with open(path, "w") as f:
        f.write("\t".join(header) + "\n")
        for row in rows:
            f.write("\t".join(row) + "\n")


def write_qualifying_specimens_tsv(
    path: Path,
    qualifying: List[SpecimenRecord],
) -> None:
    rows = []
    for r in qualifying:
        rows.append([
            r.name,
            str(r.primary_m or ""),
            f"{r.primary_err_factor:.4f}" if r.primary_err_factor is not None else "",
            r.error_model or "",
            str(r.total_input_reads or ""),
        ])
    _write_tsv(path, rows, ["specimen", "primary_RiC", "primary_err_factor",
                            "error_model", "total_input_reads"])


def write_hp_rates_tsv(
    path: Path,
    results: Dict[Tuple[str, int], ContextResult],
) -> None:
    rows = []
    for (base, length) in sorted(results.keys()):
        r = results[(base, length)]
        rows.append([
            base, str(length), str(r.n_runs), str(r.n_obs),
            f"{r.frac_correct:.6f}",
            f"{r.frac_minus1:.6f}", f"{r.frac_plus1:.6f}",
            f"{r.frac_minus2plus:.6f}", f"{r.frac_plus2plus:.6f}",
            f"{r.mean_called:.3f}", f"{r.std_called:.3f}",
        ])
    _write_tsv(path, rows, ["base", "hp_length", "n_runs", "n_obs",
                            "frac_correct", "frac_minus1", "frac_plus1",
                            "frac_minus2plus", "frac_plus2plus",
                            "mean_called", "std_called"])


def write_hp_distributions_tsv(
    path: Path,
    results: Dict[Tuple[str, int], ContextResult],
) -> None:
    rows = []
    for (base, length) in sorted(results.keys()):
        r = results[(base, length)]
        if r.n_obs == 0:
            continue
        for delta in sorted(r.distribution.keys()):
            count = r.distribution[delta]
            frac = count / r.n_obs
            rows.append([base, str(length), str(delta), str(count), f"{frac:.6f}"])
    _write_tsv(path, rows, ["base", "hp_length", "delta", "count", "fraction"])


def write_nonhp_rates_tsv(
    path: Path,
    pooled: NonHPCounts,
    by_bin: Dict[str, NonHPBin],
) -> None:
    rows = []
    denom_or = lambda counts: counts.matches + counts.substitutions + counts.deletions  # noqa: E731
    for label, bin_ in by_bin.items():
        d = denom_or(bin_.counts)
        if d <= 0:
            continue
        rows.append([
            label, str(bin_.n_clusters), str(bin_.n_reads), str(d),
            f"{bin_.counts.substitutions/d:.6f}",
            f"{bin_.counts.deletions/d:.6f}",
            f"{bin_.counts.insertions/d:.6f}",
        ])
    d_pooled = denom_or(pooled)
    if d_pooled > 0:
        rows.append([
            "POOLED", "", "", str(d_pooled),
            f"{pooled.substitutions/d_pooled:.6f}",
            f"{pooled.deletions/d_pooled:.6f}",
            f"{pooled.insertions/d_pooled:.6f}",
        ])
    _write_tsv(path, rows, ["bin", "n_clusters", "n_reads", "n_obs",
                            "sub_rate", "del_rate", "ins_rate"])


def write_debug_outputs(
    debug_dir: str,
    qualifying: List[SpecimenRecord],
    raw_hp: Dict[Tuple[str, int], ContextResult],
    filtered_hp: Dict[Tuple[str, int], ContextResult],
    pooled_nonhp: NonHPCounts,
    nonhp_by_bin: Dict[str, NonHPBin],
) -> Path:
    """Write all five debug TSVs into ``debug_dir`` (created if needed).

    Returns the directory path for the caller's summary log.
    """
    out = Path(debug_dir)
    out.mkdir(parents=True, exist_ok=True)
    write_qualifying_specimens_tsv(out / "qualifying_specimens.tsv", qualifying)
    write_hp_rates_tsv(out / "hp_error_rates.tsv", raw_hp)
    write_hp_rates_tsv(out / "hp_error_rates_filtered.tsv", filtered_hp)
    write_hp_distributions_tsv(out / "hp_distributions.tsv", raw_hp)
    write_hp_distributions_tsv(out / "hp_distributions_filtered.tsv", filtered_hp)
    write_nonhp_rates_tsv(out / "non_hp_error_rates.tsv", pooled_nonhp, nonhp_by_bin)
    return out
