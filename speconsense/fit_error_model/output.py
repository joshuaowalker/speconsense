"""YAML writer for the new error model + optional debug TSVs.

Output paths:
  - User model YAML: ``~/.config/speconsense/error_models/{name}.yaml``
    (creates the directory if it does not yet exist; refuses to overwrite
    without ``--force``).
  - Debug TSVs under ``--debug-dir`` (optional): mirrors the
    ``~/mm/analysis/hp_error_rate/output_*/tables/`` layout for continuity
    with the original analysis pipeline.
"""

from __future__ import annotations

import os
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional, Tuple

from speconsense import qctx

from .analyze import ContextResult
from .extract import NonHPCounts
from .nonhp import NonHPBin
from .selection import SpecimenRecord


def _format_yaml(
    name: str,
    chemistry: str,
    basecaller: str,
    source: str,
    dataset: str,
    description: Optional[str],
    rates: Dict[str, float],
) -> str:
    """Render the YAML body matching the shipped models' layout exactly.

    The shipped models use a header block of `#` comments above the
    name/chemistry/basecaller/source/dataset fields and the rates: mapping.
    We follow the same structure so user models remain visually consistent
    with bundled models.
    """
    lines: List[str] = []
    lines.append(f"# Context-specific error rate table for {basecaller} / {chemistry}.")
    lines.append("#")
    lines.append(f"# Generated by speconsense-fit-error-model on "
                 f"{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}.")
    if description:
        lines.append("#")
        for line in description.splitlines():
            lines.append(f"# {line}".rstrip())
    lines.append("#")
    lines.append("# Lookup keys used by speconsense:")
    lines.append("#   non-hp-sub         Substitution at a non-HP position")
    lines.append("#   non-hp-indel       Indel at a non-HP position")
    lines.append("#   hp-l{N}            Length change in an HP run of length N (1..5)")
    lines.append("# HP runs of length >= 6 are evaluated by blanket HP")
    lines.append("# normalization rather than CER (HP paper §8.1).")
    lines.append("")
    lines.append(f"name: {name}")
    lines.append(f"chemistry: {chemistry}")
    lines.append(f"basecaller: {basecaller}")
    lines.append(f"source: {source}")
    lines.append(f"dataset: {dataset}")
    lines.append("")
    lines.append("rates:")
    # Ordering matches the shipped models for diff-friendliness.
    ordered_keys = [
        "non-hp-sub",
        "non-hp-indel",
        "hp-l1",
        "hp-l2",
        "hp-l3",
        "hp-l4",
        "hp-l5",
    ]
    for key in ordered_keys:
        if key in rates:
            label = f"{key}:"
            lines.append(f"  {label:<14} {rates[key]:.4f}")
    lines.append("")
    return "\n".join(lines)


def write_model(
    name: str,
    rates: Dict[str, float],
    *,
    chemistry: str,
    basecaller: str,
    source: str,
    dataset: str,
    description: Optional[str] = None,
    force: bool = False,
    dry_run: bool = False,
) -> Tuple[Path, str]:
    """Write the YAML to ``~/.config/speconsense/error_models/{name}.yaml``.

    Returns ``(path, yaml_text)``. ``dry_run=True`` returns the rendered
    text without touching disk. Refuses to overwrite an existing file
    without ``force=True``.
    """
    user_dir = qctx.user_dir()
    user_dir.mkdir(parents=True, exist_ok=True)
    path = user_dir / f"{name}.yaml"

    text = _format_yaml(
        name=name,
        chemistry=chemistry,
        basecaller=basecaller,
        source=source,
        dataset=dataset,
        description=description,
        rates=rates,
    )

    if dry_run:
        return path, text

    if path.exists() and not force:
        raise FileExistsError(
            f"{path} already exists. Pass --force to overwrite, or pick a "
            f"different --name."
        )
    path.write_text(text)
    return path, text


